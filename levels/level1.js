// ── 关卡逻辑 ──────────────────────────────────────────────

export default {
  /**
   * @param {import('../core/Renderer.js').Renderer} renderer
   * @param {import('../core/Event.js').EventBus}    events
   */
  async start(renderer, events) {
    // 1. 渲染地图
    const map = new Map(renderer, mapData)
    await map.render()

    // 2. 启动相机，初始视角对准地图中央
    const camera = new Camera(renderer, {
      cols:     mapData.cols,
      rows:     mapData.rows,
      tileSize: mapData.tileSize,
    })
    camera.centerOn([10, 7])  // 地图正中央

    // 3. 胜负条件（阶段3由 Battle 驱动，这里先占位）
    events.on(Events.TEAM_WIPED, ({ team }) => {
      if (team === 'enemy')  events.emit(Events.LEVEL_WIN,  {})
      if (team === 'player') events.emit(Events.LEVEL_LOSE, {})
    })

    console.log('[GOS] 阶段1完成：地图渲染 + 相机系统就绪')
    console.log('[GOS] 地图尺寸：', mapData.cols, '×', mapData.rows, '格，每格', mapData.tileSize, 'px')
    console.log('[GOS] 可拖拽地图浏览全貌')
  },
}
